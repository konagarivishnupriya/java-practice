class Relational
{
    public static void main(String args[])
    {
        int x = 6;
        int y = 7;
        boolean r1 = x < y;
        boolean r2 = x > y;
        boolean r3 = x <= y;
        boolean r4 = x >= y;
        boolean r5 = x == y; // generally in maths we use '=' symbol to compare but in jave we use it as assignment operators 
        boolean r6 = x != y;
        System.out.println(r1);
        System.out.println(r2);
        System.out.println(r3);
        System.out.println(r4);
        System.out.println(r5);
        System.out.println(r6);
    }
}